package com.ecom.model;

public class Employee {
	
	private String name;
	private String job;

	
	public void setName(String name){
		this.name=name;
	}
	
	public String getFirstName(){
		return name;
	}
	
	public void setJob(String job){
		this.job=job;
	}
	
	public String getJob(){
		return job;
	}
	
	

	
}
